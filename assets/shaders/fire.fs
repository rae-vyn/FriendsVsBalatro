number cubic(const in number v, in number slope0, in number slope1) {
    number a = slope0 + slope1 - 2.;
    number b = -2. * slope0 - slope1 + 3.;
    number c = slope0;
    number v2 = v * v;
    number v3 = v * v2;
    return a * v3 + b * v2 + c * v;
}
